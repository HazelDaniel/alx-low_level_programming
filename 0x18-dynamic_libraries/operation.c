#include "list.h"

/**
  * add - the function name
  * @a: parameter of type int .
  * @b: parameter of type int .
  * Return: int .
 */
int add(int a, int b)
{
	return (a + b);
}

/**
  * sub - the function name
  * @a: parameter of type int .
  * @b: parameter of type int .
  * Return: int .
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
  * mul - the function name
  * @a: parameter of type int .
  * @b: parameter of type int .
  * Return: int .
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
  * div - the function name
  * @a: parameter of type int .
  * @b: parameter of type int .
  * Return: int .
 */
int div(int a, int b)
{
	return (a / b);
}

/**
  * mod - the function name
  * @a: parameter of type int .
  * @b: parameter of type int .
  * Return: int .
 */
int mod(int a, int b)
{
	return (a % b);
}
